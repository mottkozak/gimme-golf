interface PersonalCardInput {
  id: string
  code: string
  name: string
  description: string
  cardType: PersonalCard['cardType']
  points: number
  eligiblePars: number[]
  difficulty: PersonalCard['difficulty']
  rulesText: string
  requiredTags?: HoleTag[]
  excludedTags?: HoleTag[]
}

interface PublicCardInput {
  id: string
  code: string
  name: string
  description: string
  cardType: PublicCard['cardType']
  points: number
  eligiblePars: number[]
  rulesText: string
  requiredTags?: HoleTag[]
  excludedTags?: HoleTag[]
}

function personalCard(input: PersonalCardInput): PersonalCard {
  return {
    ...input,
    requiredTags: input.requiredTags ?? [],
    excludedTags: input.excludedTags ?? [],
    isPublic: false,
  }
}

function publicCard(input: PublicCardInput): PublicCard {
  return {
    ...input,
    difficulty: 'neutral',
    requiredTags: input.requiredTags ?? [],
    excludedTags: input.excludedTags ?? [],
    isPublic: true,
  }
}

